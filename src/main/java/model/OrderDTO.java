package model;

public class OrderDTO {
	
	private int orderId;
	private int orderNo;
	private int no_of_dish;
	
	public int getOrderId() {
		return orderId;
	}
	public void setOrderId(int orderId) {
		this.orderId = orderId;
	}
	public int getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(int orderNo) {
		this.orderNo = orderNo;
	}
	public int getNo_of_dish() {
		return no_of_dish;
	}
	public void setNo_of_dish(int no_of_dish) {
		this.no_of_dish = no_of_dish;
	}
	
	

}
